package cn.tongdun.kunpeng.api.application.phone.fuction;

/**
 * @author huangjintao
 *
 */
public class CommonConfig {
    private String proxyEnable;

    private String proxyHost;

    private String proxyPort;

    /**
     * @return the proxyEnable
     */
    public String getProxyEnable() {
        return proxyEnable;
    }

    /**
     * @param proxyEnable the proxyEnable to set
     */
    public void setProxyEnable(String proxyEnable) {
        this.proxyEnable = proxyEnable;
    }

    /**
     * @return the proxyHost
     */
    public String getProxyHost() {
        return proxyHost;
    }

    /**
     * @param proxyHost the proxyHost to set
     */
    public void setProxyHost(String proxyHost) {
        this.proxyHost = proxyHost;
    }

    /**
     * @return the proxyPort
     */
    public String getProxyPort() {
        return proxyPort;
    }

    /**
     * @param proxyPort the proxyPort to set
     */
    public void setProxyPort(String proxyPort) {
        this.proxyPort = proxyPort;
    }

  
   
    
}
